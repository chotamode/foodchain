package foodchain.transactions;

/**
 * The enum Transaction type.
 */
public enum TransactionType {
    /**
     * Product transaction type.
     */
    PRODUCT,

    /**
     * Money transaction type.
     */
    MONEY,

    /**
     * Transport transaction type.
     */
    TRANSPORT,

    /**
     * Want beef transaction type.
     */
    WANT_BEEF,
    /**
     * Want pork transaction type.
     */
    WANT_PORK,
    /**
     * Want chicken transaction type.
     */
    WANT_CHICKEN,

    /**
     * Want milk transaction type.
     */
    WANT_MILK,
    /**
     * Want yoghurt transaction type.
     */
    WANT_YOGHURT,
    /**
     * Want cream transaction type.
     */
    WANT_CREAM,

    /**
     * Want tomatoes transaction type.
     */
    WANT_TOMATOES,
    /**
     * Want potatoes transaction type.
     */
    WANT_POTATOES,
    /**
     * Want onion transaction type.
     */
    WANT_ONION
}
